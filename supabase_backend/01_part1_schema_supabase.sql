create table if not exists public.hwl_customer (
  cust_id integer generated by default as identity primary key,
  user_id uuid unique references public.hwl_app_profile(user_id) on delete set null,
  cust_fname varchar(50) not null,
  cust_lname varchar(50) not null,
  street varchar(150) not null,
  city varchar(50) not null,
  state char(2) not null,
  zip_code varchar(10) not null,
  gender char(1),
  marital_status char(1) not null,
  cust_type char(1) not null,
  email text unique,
  phone text,
  constraint chk_cust_type check (cust_type in ('A', 'H', 'B')),
  constraint chk_gender check (gender in ('M', 'F') or gender is null),
  constraint chk_marital check (marital_status in ('M', 'S', 'W'))
);

comment on table public.hwl_customer is 'Customer master data for NICE insurance services.';
comment on column public.hwl_customer.cust_type is 'Customer type: A=Auto only, H=Home only, B=Both Auto and Home.';

create table if not exists public.hwl_auto_policy (
  policy_id integer generated by default as identity primary key,
  cust_id integer not null references public.hwl_customer(cust_id) on delete restrict,
  start_date date not null,
  end_date date not null,
  premium numeric(10,2) not null,
  policy_status char(1) not null,
  constraint chk_auto_dates check (end_date > start_date),
  constraint chk_auto_policy_status check (policy_status in ('C', 'E')),
  constraint chk_auto_premium check (premium >= 0)
);

comment on table public.hwl_auto_policy is 'Auto insurance policy header; stores current and expired policies.';
comment on column public.hwl_auto_policy.policy_status is 'Policy status: C=Current, E=Expired.';

create table if not exists public.hwl_home_policy (
