-- Supabase/PostgreSQL version of the Part I Oracle DDL.
-- This is the recommended backend schema for Part II because it preserves
-- the original Part I HWL_* tables and adds only the web-app tables needed
-- for Supabase Auth, employees, claims, RLS, and demo workflows.

create extension if not exists "pgcrypto";

-- ============================================================
-- 1. Supabase Auth Mapping Tables
-- ============================================================

create table if not exists public.hwl_app_profile (
  user_id uuid primary key references auth.users(id) on delete cascade,
  role text not null default 'customer' check (role in ('customer', 'employee')),
  email text not null unique,
  full_name text,
  phone text,
  created_at timestamptz not null default now()
);

create table if not exists public.hwl_employee (
  emp_id integer generated by default as identity primary key,
  user_id uuid not null unique references public.hwl_app_profile(user_id) on delete cascade,
  emp_fname varchar(50) not null,
  emp_lname varchar(50) not null,
  email text not null unique,
  phone text,
  department text not null default 'Customer Service',
  job_title text not null default 'Insurance Specialist',
  created_at timestamptz not null default now()
);

