create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_full_name text;
  v_fname text;
  v_lname text;
begin
  v_full_name := coalesce(new.raw_user_meta_data ->> 'full_name', split_part(new.email, '@', 1));
  v_fname := split_part(v_full_name, ' ', 1);
  v_lname := nullif(trim(replace(v_full_name, v_fname, '')), '');
  v_lname := coalesce(v_lname, 'Customer');

  insert into public.hwl_app_profile (user_id, role, email, full_name)
  values (new.id, 'customer', new.email, v_full_name)
  on conflict (user_id) do nothing;

  insert into public.hwl_customer (
    user_id, cust_fname, cust_lname, street, city, state, zip_code,
    gender, marital_status, cust_type, email
  )
  values (
    new.id, v_fname, v_lname, 'Unknown Street', 'New York', 'NY', '10001',
    null, 'S', 'B', new.email
  )
  on conflict (user_id) do nothing;

  return new;
end;
$$;

