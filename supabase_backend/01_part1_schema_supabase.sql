create or replace function public.submit_auto_claim(
  p_policy_id integer,
  p_claim_amount numeric,
  p_description text
)
returns table (claim_id integer, claim_status text)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_cust_id integer;
  v_policy hwl_auto_policy%rowtype;
  v_claim_id integer;
begin
  v_cust_id := public.current_cust_id();
  if v_cust_id is null then
    raise exception 'Only customers can submit claims';
  end if;

  select * into v_policy
  from public.hwl_auto_policy
  where policy_id = p_policy_id
  for update;

  if not found or v_policy.cust_id <> v_cust_id then
    raise exception 'Auto policy not found or not owned by current customer';
  end if;

  if v_policy.policy_status <> 'C' then
    raise exception 'Only current policies can receive claims';
  end if;

  insert into public.hwl_claim (
    claim_type, auto_policy_id, cust_id, assigned_emp_id,
    claim_amount, description, claim_status
  )
  values (
    'auto',
    p_policy_id,
    v_cust_id,
    (select emp_id from public.hwl_employee order by created_at limit 1),
    p_claim_amount,
    p_description,
    'submitted'
  )
  returning hwl_claim.claim_id into v_claim_id;

  insert into public.hwl_claim_history (claim_id, claim_status, note, changed_by)
  values (v_claim_id, 'submitted', 'Auto claim submitted by customer', auth.uid());

  return query select v_claim_id, 'submitted'::text;
end;
$$;

