create table if not exists public.hwl_claim (
  claim_id integer generated by default as identity primary key,
  claim_type text not null check (claim_type in ('auto', 'home')),
  auto_policy_id integer references public.hwl_auto_policy(policy_id) on delete restrict,
  home_policy_id integer references public.hwl_home_policy(policy_id) on delete restrict,
  cust_id integer not null references public.hwl_customer(cust_id) on delete restrict,
  assigned_emp_id integer references public.hwl_employee(emp_id) on delete set null,
  claim_amount numeric(10,2) not null check (claim_amount > 0),
  description text not null,
  claim_status text not null default 'submitted' check (claim_status in ('submitted', 'under_review', 'approved', 'rejected', 'paid')),
  submitted_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint chk_claim_one_policy check (
    (claim_type = 'auto' and auto_policy_id is not null and home_policy_id is null)
    or
    (claim_type = 'home' and home_policy_id is not null and auto_policy_id is null)
  )
);

create table if not exists public.hwl_claim_history (
  history_id integer generated by default as identity primary key,
  claim_id integer not null references public.hwl_claim(claim_id) on delete cascade,
  claim_status text not null,
  note text,
  changed_by uuid not null references public.hwl_app_profile(user_id) on delete restrict,
  changed_at timestamptz not null default now()
);

