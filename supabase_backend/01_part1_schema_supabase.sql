create or replace function public.review_claim(
  p_claim_id integer,
  p_new_status text,
  p_note text default null
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_emp_id integer;
begin
  v_emp_id := public.current_emp_id();
  if v_emp_id is null then
    raise exception 'Only employees can review claims';
  end if;

  if p_new_status not in ('under_review', 'approved', 'rejected', 'paid') then
    raise exception 'Invalid claim review status';
  end if;

  update public.hwl_claim
  set claim_status = p_new_status,
      assigned_emp_id = coalesce(assigned_emp_id, v_emp_id)
  where claim_id = p_claim_id;

  if not found then
    raise exception 'Claim not found';
  end if;

  insert into public.hwl_claim_history (claim_id, claim_status, note, changed_by)
  values (p_claim_id, p_new_status, p_note, auth.uid());
end;
$$;

create or replace function public.pay_auto_invoice(
  p_invoice_id integer,
  p_pay_method varchar
)
returns table (payment_id integer, invoice_status text)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_invoice hwl_auto_invoice%rowtype;
  v_payment_id integer;
begin
  select * into v_invoice
  from public.hwl_auto_invoice
  where invoice_id = p_invoice_id
  for update;

  if not found then
    raise exception 'Auto invoice not found';
  end if;

  if not (
    public.is_employee()
    or exists (
      select 1 from public.hwl_auto_policy p
      where p.policy_id = v_invoice.policy_id
        and p.cust_id = public.current_cust_id()
    )
  ) then
    raise exception 'Not authorized to pay this invoice';
  end if;

  if v_invoice.invoice_status = 'paid' then
    raise exception 'Invoice is already paid';
  end if;

  insert into public.hwl_auto_payment (invoice_id, payment_date, pay_method)
  values (p_invoice_id, current_date, p_pay_method)
  returning hwl_auto_payment.payment_id into v_payment_id;

  update public.hwl_auto_invoice
  set invoice_status = 'paid'
  where invoice_id = p_invoice_id;

  return query select v_payment_id, 'paid'::text;
end;
$$;

